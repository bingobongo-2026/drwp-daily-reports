/**
 * 対応SNS。slug => array( ラベル, 既定の表示順 )。
 */
function jijipom_social_networks() {
	return array(
		'x'         => array( __( 'X (旧Twitter)', 'jijipom' ), 1 ),
		'instagram' => array( __( 'Instagram', 'jijipom' ), 2 ),
		'facebook'  => array( __( 'Facebook', 'jijipom' ), 3 ),
		'threads'   => array( __( 'Threads', 'jijipom' ), 4 ),
		'tiktok'    => array( __( 'TikTok', 'jijipom' ), 5 ),
		'youtube'   => array( __( 'YouTube', 'jijipom' ), 6 ),
	);
}

/**
 * カスタマイザーに「ソーシャルメディア」セクションを登録。
 * 各SNSごとに「リンク先URL」と「表示順(1〜6)」を用意する。
 */
function jijipom_customize_register_social( $wp_customize ) {
	$wp_customize->add_section(
		'jijipom_social',
		array(
			'title'       => __( 'ソーシャルメディア', 'jijipom' ),
			'description' => __( 'URLを入力したSNSだけがフッター右下にアイコン表示されます。表示順の数字(小さいほど左)で並び順を変えられます。', 'jijipom' ),
			'priority'    => 47,
		)
	);

	$priority = 10;
	foreach ( jijipom_social_networks() as $slug => $data ) {
		list( $label, $default_order ) = $data;

		$wp_customize->add_setting(
			"jijipom_social_{$slug}_url",
			array(
				'default'           => '',
				'sanitize_callback' => 'esc_url_raw',
				'transport'         => 'refresh',
			)
		);
		$wp_customize->add_control(
			"jijipom_social_{$slug}_url",
			array(
				/* translators: %s: SNS名 */
				'label'    => sprintf( __( '%s : リンク先URL', 'jijipom' ), $label ),
				'section'  => 'jijipom_social',
				'type'     => 'url',
				'priority' => $priority,
			)
		);

		$wp_customize->add_setting(
			"jijipom_social_{$slug}_order",
			array(
				'default'           => $default_order,
				'sanitize_callback' => 'absint',
				'transport'         => 'refresh',
			)
		);
		$wp_customize->add_control(
			"jijipom_social_{$slug}_order",
			array(
				/* translators: %s: SNS名 */
				'label'       => sprintf( __( '%s : 表示順', 'jijipom' ), $label ),
				'section'     => 'jijipom_social',
				'type'        => 'number',
				'input_attrs' => array(
					'min'  => 1,
					'max'  => 6,
					'step' => 1,
				),
				'priority'    => $priority + 1,
			)
		);

		$priority += 10;
	}
}
add_action( 'customize_register', 'jijipom_customize_register_social' );

/**
 * 表示すべきSNSリンクを表示順に並べて返す。
 * URL未入力のSNSは除外。表示順の数字→既定順の順でソート。
 *
 * @return array<int, array{slug:string,label:string,url:string}>
 */
function jijipom_social_links() {
	$items = array();
	foreach ( jijipom_social_networks() as $slug => $data ) {
		$url = get_theme_mod( "jijipom_social_{$slug}_url", '' );
		if ( ! $url ) {
			continue;
		}
		$order = absint( get_theme_mod( "jijipom_social_{$slug}_order", $data[1] ) );
		$items[] = array(
			'slug'          => $slug,
			'label'         => $data[0],
			'url'           => $url,
			'order'         => $order ? $order : $data[1],
			'default_order' => $data[1],
		);
	}
	usort(
		$items,
		function ( $a, $b ) {
			if ( $a['order'] === $b['order'] ) {
				return $a['default_order'] <=> $b['default_order'];
			}
			return $a['order'] <=> $b['order'];
		}
	);
	return $items;
}

